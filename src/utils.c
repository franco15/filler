#include "filler.h"

void	get_init_coords(t_filler *f)
{
	if (f->mx == 17)
	{
		f->px = f->yo == 1 ? 2 : 15;
		f->py = f->yo == 1 ? 9 : 13;
	}
	else if (f->mx == 40)
	{
		f->px = f->yo == 1 ? 4 : 33;
		f->py = f->yo == 1 ? 4 : 20;
	}
	else if (f->mx == 99)
	{
		f->px = f->yo == 1 ? 31 : 71;
		f->py = f->yo == 1 ? 30 : 70;
	}
}
